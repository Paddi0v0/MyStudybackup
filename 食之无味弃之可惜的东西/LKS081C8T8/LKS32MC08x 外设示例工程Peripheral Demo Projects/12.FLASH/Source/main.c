uint32_t Read_BUFF[128] = {0};
u8 key = 0;
int main(void)
{

	u32 i = 0;
	Hardware_init(); /* 硬件初始化 */
	while (1)
	{
		key = GPIO_KEY_Scan(0);
		switch (key)
		{
		case start_PRES:
			/********************Flash数据擦除*****************************************/
			printf("正在擦除数据，请稍等 \n");
			erase_flag = 0x9A0D361F;				 //输入擦除密码，防止程序跑飞
			EraseSector(Flash_ADDR, Flash_MAIN_NVR); //NVR只能块擦除 用户NVR有1个扇区，Sector1首地址为0x0
		  erase_flag = 0;
			printf("数据擦除完成\r\n");
			/**************************************************************************/

			/********************Flash数据写入*****************************************/
			printf("正在写入数据，请稍等 \n");
			progm_flag = 0x9AFDA40C; //输入编程密码，防止程序跑飞
			ProgramPage(Flash_ADDR, Flash_SIZE, NVR_MAIN_BUFF, Flash_MAIN_NVR);
		  progm_flag = 0;
			printf("数据写入完成");
			/**************************************************************************/
			break;

		case stop_PRES:
			/********************Flash数据读取*****************************************/
			printf("正在读取数据，请稍等\n");
		  //该函数读取NVR是读取用户区，芯片校正参数请调用Read_Trim();函数
			Read_More_Flash(Flash_ADDR, Flash_SIZE / 4, Read_BUFF, Flash_MAIN_NVR);
			for (i = 0; i < (Flash_SIZE / 4); i++)
			{
				printf("Hellow word = %x\r\n ", Read_BUFF[i]);
			}
			printf("数据读取完成\r\n");
			/**************************************************************************/
			break;
		}
		SoftDelay(0x1000);
	}
}

