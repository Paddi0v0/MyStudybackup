/*******************************************************************************
 函数名称：    void TIMER3_IRQHandler(void)
 功能描述：    TIMER3中断处理函数
 输入参数：    无
 输出参数：    无
 返 回 值：    无
 其它说明：
 修改日期      版本号          修改人            修改内容
 -----------------------------------------------------------------------------
 2020/8/5      V1.0           Howlet Li          创建
 *******************************************************************************/
void UTIMER3_IRQHandler(void)
{
	static u8 s_Limitdutyflag = 0;        //极限占空比标志，100%或者0%占空比
	if(UTIMER_IF & BIT9)                  //判断UTimer0是否发生过零中断  ,96000000/96000000 = 1s 1KHZ,每1s进入一次过零中断
	{//中断进入一次为1s
	    UTIMER_IF = BIT9;                 //清除UTimer中断标志位  
      if(s_Limitdutyflag == 0)          //高低电平持续时间大于100*1ms = 100ms，
			{
			   if(SPsetup_Voltage != 0)       //检测一直输出为高，
				 {
				    gS_PWMFreqCat.LEVEL_HL = 1; //最大限定输出100%占空比
				 }else 
				 {
					  gS_PWMFreqCat.LEVEL_HL = 2; //最小限定输出0%占空比  
				 }
			}
      s_Limitdutyflag = 0;		
	}
	if(UTIMER_IF & BIT10)                  //判断UTimer3的CH0是否发生捕获上升沿中断
	{		
       Utimer3_CapFrwq(&s_Limitdutyflag);//获取PWM高电平和低电平时间计数值
			 UTIMER_IF = BIT10;
	}
}




