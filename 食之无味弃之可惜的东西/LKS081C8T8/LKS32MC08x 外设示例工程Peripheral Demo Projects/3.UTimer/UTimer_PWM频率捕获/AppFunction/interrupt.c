/*******************************************************************************
 函数名称：    void TIMER0_IRQHandler(void)
 功能描述：    TIMER0中断处理函数
 输入参数：    无
 输出参数：    无
 返 回 值：    无
 其它说明：
 修改日期      版本号          修改人            修改内容
 -----------------------------------------------------------------------------
 2021/8/25      V1.0          HMG               创建
 *******************************************************************************/
u32 g_wuInPut_PulseFreq = 0; //脉冲周期计数值
void UTIMER0_IRQHandler(void)
{

	static u32 OverFlow = 0;		  //在输入捕获中，计数器溢出几次
	static u8 Start_flag = 0;		  //捕获第一个上升沿标志
	static u32 InPut_FrontTime = 0;	  //捕获上一次脉冲对应的计数值
	static u32 InPut_CurrentTime = 0; //捕获当前脉冲对应的计数值
	if (UTIMER_IF & BIT0) //判断UTimer0是否发生过零中断  ,2KHZ,每0.5ms进入一次过零中断
	{					  //中断进入一次为0.5ms
		UTIMER_IF = BIT0; //清除UTimer中断标志位
		OverFlow += 1;
	}
	
	if (UTIMER_IF & BIT2) //判断UTimer0的CH1是否发生捕获上升沿中断 TIM0_CH1: P0.14
	{		
		if (Start_flag == 0)   //捕获第一个上升沿
		{
			InPut_FrontTime = UTIMER_UNT0_CMP1;
			OverFlow = 0;       //清除捕获计数器溢出次数
			Start_flag = 1;
		}
		else //捕获第二个上升沿
		{		
			InPut_CurrentTime = UTIMER_UNT0_CMP1;
      Start_flag = 0;
			if (OverFlow == 0) //捕获时间无溢出
			{
				g_wuInPut_PulseFreq = (InPut_CurrentTime - InPut_FrontTime); //脉冲周期计数值
				
			}
			else //捕获时间有溢出
			{
				g_wuInPut_PulseFreq = (UTIMER_UNT0_TH * OverFlow - InPut_FrontTime + InPut_CurrentTime); //脉冲周期计数值
			}
		}
		UTIMER_IF = BIT2;	 //清除UTimer中断标志位
	}
	
  
}

