u8 tx_table[8] = {0x00, 0x12, 0x34, 0x56, 0x78, 0x9a, 0xbc, 0xd1};
u8 rx_table[sizeof(tx_table)+1];                     //接收数据，接收数组申请大小必须比发送数据字节个数多
int main(void)
{
	Hardware_init();
	while (1)
	{
		Spi_master_DMA((u8 *)tx_table, (u8 *)rx_table, sizeof(tx_table));
		SoftDelay(0xfff);
	}
}

