UINT8 Tx_data_tbl[8] = {0xf3, 0x3f, 0x11, 0x22, 0x23, 0x44, 0x56, 0x80}; //数据发送缓冲区
UINT8 Rx_data_tbl[8] = {0};												                       //接收数据缓冲区
extern u8 spi_flag;
int main(void)
{
	Hardware_init();
	while (1)
	{
		Spi_master_DMA(Tx_data_tbl, Rx_data_tbl, 8);
		SoftDelay(0xfffff);
	}
}

