int main(void)
{
	if(!ConfigData_check())				/*	检查配置参数						*/
	{
		while(1);						/*	配置参数或库不支持，进入死循环状态	*/
	}
	__disable_irq();      /* 关闭中断 中断总开关 */
    Hardware_init();      /* 硬件初始化 */
    sys_init();           /* 系统初始化 */
    __enable_irq();						/* 使能中断 */

    for(;;)
    {
        Task_Scheduler(); /* 任务调度函数，根据时间分片处理 */
    }
}

