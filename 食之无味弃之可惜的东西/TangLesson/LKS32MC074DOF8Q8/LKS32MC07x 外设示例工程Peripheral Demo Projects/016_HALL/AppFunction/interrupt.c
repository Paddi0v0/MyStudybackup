u8 HALL_DATA = 0;
void HALL0_IRQHandler(void)
{
	 while(HALL_CAPTURE_EVENT == HALL_GetIRQFlag(HALL_CAPTURE_EVENT))//HALL信号变化事件标志 
	 { 
			HALL_DATA = HALL0_INFO&0x07; //读取HALL的滤波结果值
		  HALL_Clear_IRQ(HALL_CAPTURE_EVENT);
	 }
		while(HALL_OVERFLOW_EVENT == HALL_GetIRQFlag(HALL_OVERFLOW_EVENT))//计数器溢出中断
	 {
			HALL_Clear_IRQ(HALL_OVERFLOW_EVENT);
	 }
}


