u16 DMAIRQCnt = 0;
void DMA0_IRQHandler(void)
{
	if (DMA0_IF & BIT0) //DMA通道3完成中断标志
	{
		DMA0_IF = BIT0;
		DMA_CHx_EN(DMA_CHN0,DISABLE);  /*使能DMA_CH0通道*/
	}
	else if (DMA0_IF & BIT1) //DMA通道1完成中断标志
	{
		DMA0_IF = BIT1;
		
		DMAIRQCnt++;
	}
	else if (DMA0_IF & BIT2) //DMA通道3完成中断标志
	{
		DMA0_IF = BIT2;
		DMA_CHx_EN(DMA_CHN2,DISABLE);  /*使能DMA_CH2通道*/
	}
	else if (DMA0_IF & BIT3) //DMA通道3完成中断标志
	{
		DMA0_IF = BIT3;
		DMA_CHx_EN(DMA_CHN3,DISABLE);  /*使能DMA_CH3通道*/
	}
}
