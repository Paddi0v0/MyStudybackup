s16 ADCSample[10] = {0};
void ADC0_IRQHandler(void)
{
	  if(ADC_GetIRQFlag(ADC0,ADC_SF1_IF))
		{
		    ADC_ClearIRQFlag(ADC0,ADC_SF1_IF);
			  ADCSample[0] = ADC_GetConversionValue(ADC0,DAT0) ;
				ADCSample[1] = ADC_GetConversionValue(ADC0,DAT1) ;
		}
		else if(ADC_GetIRQFlag(ADC0,ADC_SF2_IF))
		{
		    ADC_ClearIRQFlag(ADC0,ADC_SF2_IF);
			  ADCSample[2] = ADC_GetConversionValue(ADC0,DAT2) ;
				ADCSample[3] = ADC_GetConversionValue(ADC0,DAT3) ;
		}
		else
		{
		    ADC_ClearIRQFlag(ADC0,ADC_ALL_IF);
		}
}

